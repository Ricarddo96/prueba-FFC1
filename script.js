// ¿Qué es JavaScript?
// JavaScript es un lenguaje de programación utilizado para hacer que las páginas web sean interactivas.
// Puedes usar JavaScript para realizar cálculos, manipular el DOM (Document Object Model), 
// y trabajar con datos de varias formas, entre otras cosas.

// ¿Qué es una constante?
// Una constante es un valor que no puede ser reasignado una vez que se le ha dado un valor.
// A diferencia de una variable, su valor es inmutable después de su definición.

const saludo = "Hola Bros"; // Aquí estamos declarando una constante llamada greeting, cuyo valor es "Hola Bros"

// Imprimimos el valor de la constante en la consola (mira la consola del navegador)
console.log(saludo);

